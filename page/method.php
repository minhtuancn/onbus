<h1><strong>Payment Method</strong></h1>

<p>You can pay by the following methods:</p>

<p><strong>1. International Payment Card</strong></p>

<p>You can pay by International Payment Card when booking a ticket at OnBus.vn. We accept MasterCard, VISA, American Express and JCB (Japan Credit Bureau).</p>
<p>
After you have selected your booking, choose 'International Payment Card' under the payment options. 
You will be prompted to enter your card details on the next page. Next, select the organisation that issued your card. 
Enter all necessary details and confirm your booking. Once your payment has been authorized, you will be sent an e-ticket by e-mail.
</p>
<p><strong>Security</strong><br/>
Your payment details will be encoded and sent using a secure connection. 
You can check the security details by clicking with your right mouse button on the payment page and then selecting ‘Properties’. 
A window will pop up containing all connection data. The ‘Connection’ field shows that an SSL connection is used. 
Your credit card information, including the number and expiry date, will not be stored.
</p>

<p><strong>2. Online Payment – Viet Nam Local Banking</strong></p>
<p>With online banking, you pay through our partner SmartLink.</p>
<p>After you have selected your booking, You will be prompted to enter your card details on the next page. 
Select your bank and enter all required information. Once payment has been confirmed, the amount will be debited from your account. 
Once the bank informs us that payment has been made, we will send your e-ticket to you by e-mail. This process usually takes less than 1 hour.</p>

<p><strong>3. Cash payment</strong></p>
<p>You can pay by cash after you have made your online booking. OnBus officer will call you to confirm your booking and your address. 
Please make sure your information (address and phone) is correct.</p>
<p>You then pay and retrieve your ticket (hard-coppy) at your address – OnBus delivery ticket. 
This payment term is applied for persons living in Vietnam and applied a fee of ticket delivery.</p>

<h1><strong>About SMARTLINK</strong></h1>

<p>Smartlink is an organisation that processes online payments and payment information on our behalf.
 This includes Real Time Online Banking and bank transfers.</p>
 <p>If you pay through Smartlink, we will issue your ticket and send it to you by e-mail once we have received confirmation of full payment.  </p>
 <p>Your payment details will be sent safely to Smartlink and will only be used to process the transaction. 
 This information will never be disclosed or forwarded to third parties nor used for commercial purposes.</p>
 <p>We use the online services of the worldwide Smartlink network to ensure that your payment 
 takes place as efficiently and safely as possible.</p>


