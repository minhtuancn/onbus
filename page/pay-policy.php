<h1><strong>Cancellation Policy</strong></h1>

<p>On Bus is not responsible for the following cases:</p>

<ul>
	<li>Time delay is caused by the bus operator.</li>
	<li>Bus operator cancellation due to weather issues, politics and other valid reasons.</li>
	<li>Customers' baggage lost / damaged</li>
	<li>The car seat changing customer at the last minute to make room for women and children, the elderly, the disabled or the sick.</li>
	<li>Address passenger provided false or does not exist</li>
</ul>

<p>When you cancel tickets booked through <a href="http://www.onbus.vn"><span style="color:#800080"><u><strong>OnBus</strong></u></span></a> Please note that the money is not transferred back to your credit / debit card passengers. Instead, the money will be refunded in the form of coupons that can be used in your next transaction. This coupon valid up to 6 months from the date of cancellation.</p>

<p>The following are the cancellation fees:</p>

<ul>
	<li>From 24 hours to 3 days before the journey is a 10% cancellation fee.</li>
	<li>From 3 days to 1 week before the journey is 5% cancellation fee.</li>
</ul>

<p>Please note that the cancellation fee and cancellation period may differ according to each car.</p>

<p>* The transaction costs or home delivery charges will not be refunded in case of cancellation</p>

<p>In case of confirmation e-mails and messages delayed or unsuccessful due to technical reasons or as a result of e-mail ID / phone incorrectly provided by the user, etc., tickets will be considered a "ticket was placed" as long as the ticket appears on the confirmation page <a href="#">www.onbus.vn</a></p>

<p>Lawsuits and complaints related to travel must be reported to the support group <a href="http://www.onbus.vn"><span style="color:#800080"><u><strong>OnBus</strong></u></span></a> within 10 days from the date of your travel.</p>

